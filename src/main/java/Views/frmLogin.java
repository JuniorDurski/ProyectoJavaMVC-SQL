    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = jTextField1.getText().trim();
        String clave = String.valueOf(jTextField2.getPassword()).trim(); // Obtener la contraseña como String
        if (usuario.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese usuario y clave", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ClienteCtr clienteCtr = new ClienteCtr();
        int idCliente = clienteCtr.login(usuario, clave);
        //int idCliente = cliente.getId(); // Obtener el ID del cliente
            

        if (idCliente != 0) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            frmMenu newFrame = new frmMenu(idCliente,0);
            newFrame.setVisible(true);
            this.dispose();

        } else {
            //login Empleado
            EmpleadoCtr empleadoCtr = new EmpleadoCtr();
            int idEmpleado = empleadoCtr.login(usuario, clave);
            
            
            if (idEmpleado != 0) {
                JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                int idRole = empleadoCtr.role(idEmpleado);
                frmMenu newFrame = new frmMenu(idEmpleado,idRole);
                newFrame.setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

